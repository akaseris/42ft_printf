#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char				*str;
	unsigned int		i;

	if (s)
	{
		if ((str = (char *)malloc(len + 1)) == NULL)
			return (NULL);
		i = 0;
		while (len--)
		{
			str[i] = s[start + i];
			i++;
		}
		str[i] = '\0';
		return (str);
	}
	return (NULL);
}
