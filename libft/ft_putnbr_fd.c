#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char c;

	if (n < 0)
	{
		if (n == -2147483648)
		{
			write(fd, "-", 1);
			write(fd, "2", 1);
			n = 147483648;
		}
		else
		{
			write(fd, "-", 1);
			n = -n;
		}
	}
	if (n > 9)
		ft_putnbr_fd(n / 10, fd);
	c = n % 10 + '0';
	write(fd, &c, 1);
}
