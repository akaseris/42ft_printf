#include "libft.h"

void	ft_putnbr(int n)
{
	char c;

	if (n < 0)
	{
		if (n == -2147483648)
		{
			write(1, "-", 1);
			write(1, "2", 1);
			n = 147483648;
		}
		else
		{
			write(1, "-", 1);
			n = -n;
		}
	}
	if (n > 9)
		ft_putnbr(n / 10);
	c = n % 10 + '0';
	write(1, &c, 1);
}
