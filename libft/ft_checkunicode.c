#include "libft.h"

int		ft_checkunicode(wchar_t *str)
{
	int i;

	if (!str)
		return (0);
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] > 0x7F)
			return (1);
		i++;
	}
	return (0);
}
